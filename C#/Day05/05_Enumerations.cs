// Enumerations(Enums) --> Represent fixed set of constants
/*
An Enumeration (enum) is a special value type in C# that defines a set of named constant values. 
It is used to represent a fixed collection of related values in a type-safe, readable, and 
meaningful way instead of using raw numbers or strings.
In simple terms, enums replace magic numbers with clear, self-descriptive names, improving code clarity and safety.
*/


/*

enum Days
{
    Monday, Tuesday, Wednesday, Thursday, Friday
}


// usage 
Days today = Days.Monday;
Console.WriteLine(today);

